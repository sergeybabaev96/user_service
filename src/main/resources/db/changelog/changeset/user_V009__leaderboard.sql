CREATE TABLE IF NOT EXISTS user_activity (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL,
    last_updated TIMESTAMP NOT NULL,
    rating BIGINT NOT NULL,
    CONSTRAINT fk_user_activity_user FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE ON UPDATE CASCADE
);

 CREATE TABLE IF NOT EXISTS user_popularity (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL UNIQUE,
    last_updated TIMESTAMP NOT NULL,
    impact BIGINT NOT NULL,
    CONSTRAINT fk_user_popularity_user FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE ON UPDATE CASCADE
);